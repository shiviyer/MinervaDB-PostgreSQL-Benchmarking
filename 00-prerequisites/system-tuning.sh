#!/usr/bin/env bash
# =============================================================================
# MinervaDB PostgreSQL Benchmarking Toolkit
# system-tuning.sh - OS-level tuning for accurate benchmarking
# =============================================================================
# IMPORTANT: Run this on the PostgreSQL server BEFORE benchmarking.
# These settings optimize the OS for consistent, reproducible benchmarks.
# =============================================================================

set -euo pipefail

RED='\033[0;31m'; GREEN='\033[0;32m'; YELLOW='\033[1;33m'
BLUE='\033[0;34m'; BOLD='\033[1m'; NC='\033[0m'

log() { echo -e "${BLUE}[INFO]${NC} $1"; }
success() { echo -e "${GREEN}[SUCCESS]${NC} $1"; }
warn() { echo -e "${YELLOW}[WARN]${NC} $1"; }

# =============================================================================
# Kernel Parameters (/etc/sysctl.conf)
# =============================================================================

apply_kernel_params() {
    log "Applying kernel parameters for PostgreSQL benchmarking..."

    cat >> /etc/sysctl.d/99-minervadb-postgres-benchmark.conf << 'EOF'
# MinervaDB PostgreSQL Benchmarking Kernel Parameters
# Generated by MinervaDB PostgreSQL Benchmarking Toolkit

# --- Shared Memory ---
kernel.shmmax = 274877906944      # 256GB - adjust to your RAM
kernel.shmall = 67108864          # 256GB / PAGE_SIZE
kernel.shmmni = 4096

# --- Huge Pages ---
vm.nr_hugepages = 8192            # Adjust: RAM_for_PG / 2MB
vm.hugetlb_shm_group = 0

# --- Virtual Memory ---
vm.swappiness = 1                 # Minimize swapping
vm.dirty_ratio = 10               # % dirty pages before writeback
vm.dirty_background_ratio = 3    # Background writeback threshold
vm.dirty_expire_centisecs = 500  # Time before dirty pages written
vm.dirty_writeback_centisecs = 100

# --- Overcommit ---
vm.overcommit_memory = 2         # Don't overcommit
vm.overcommit_ratio = 80         # % of RAM available for overcommit

# --- Network (for pgbouncer/remote clients) ---
net.core.somaxconn = 65535
net.core.rmem_max = 134217728
net.core.wmem_max = 134217728
net.ipv4.tcp_rmem = 4096 87380 134217728
net.ipv4.tcp_wmem = 4096 65536 134217728
net.ipv4.tcp_max_syn_backlog = 8192

# --- File Descriptors ---
fs.file-max = 2097152
fs.aio-max-nr = 1048576
EOF

    sysctl -p /etc/sysctl.d/99-minervadb-postgres-benchmark.conf
    success "Kernel parameters applied"
}

# =============================================================================
# I/O Scheduler
# =============================================================================

configure_io_scheduler() {
    log "Configuring I/O scheduler..."
    
    for DISK in $(lsblk -d -o NAME | grep -v NAME | grep -v loop); do
        DISK_PATH="/sys/block/${DISK}"
        
        if [ -f "${DISK_PATH}/queue/rotational" ]; then
            ROTATIONAL=$(cat "${DISK_PATH}/queue/rotational")
            
            if [ "${ROTATIONAL}" = "0" ]; then
                # SSD/NVMe: use 'none' or 'mq-deadline'
                echo "none" > "${DISK_PATH}/queue/scheduler" 2>/dev/null || \
                echo "mq-deadline" > "${DISK_PATH}/queue/scheduler" 2>/dev/null
                log "${DISK}: SSD detected, using 'none/mq-deadline' scheduler"
            else
                # HDD: use 'deadline' or 'mq-deadline'  
                echo "mq-deadline" > "${DISK_PATH}/queue/scheduler" 2>/dev/null || \
                echo "deadline" > "${DISK_PATH}/queue/scheduler" 2>/dev/null
                log "${DISK}: HDD detected, using 'mq-deadline' scheduler"
            fi
            
            # Set queue depth
            echo 256 > "${DISK_PATH}/queue/nr_requests" 2>/dev/null || true
            echo 256 > "${DISK_PATH}/queue/iosched/nr_requests" 2>/dev/null || true
        fi
    done
    
    success "I/O scheduler configured"
}

# =============================================================================
# CPU Frequency Scaling
# =============================================================================

configure_cpu_performance() {
    log "Setting CPU governor to 'performance' for consistent benchmarks..."
    
    if command -v cpupower &>/dev/null; then
        cpupower frequency-set -g performance 2>/dev/null || true
        success "CPU governor set to performance via cpupower"
    elif ls /sys/devices/system/cpu/cpu*/cpufreq/scaling_governor 2>/dev/null | head -1; then
        for GOV in /sys/devices/system/cpu/cpu*/cpufreq/scaling_governor; do
            echo "performance" > "${GOV}" 2>/dev/null || true
        done
        success "CPU governor set to performance"
    else
        warn "Cannot set CPU governor - cpupower not available"
    fi
    
    # Disable C-states for lowest latency
    if [ -f /sys/devices/system/cpu/cpu0/cpuidle/state1/disable ]; then
        for STATE in /sys/devices/system/cpu/cpu*/cpuidle/state*/disable; do
            echo 1 > "${STATE}" 2>/dev/null || true
        done
        log "C-states disabled for benchmark consistency"
    fi
}

# =============================================================================
# NUMA Configuration
# =============================================================================

configure_numa() {
    log "Checking NUMA topology..."
    
    if command -v numactl &>/dev/null; then
        NUMA_NODES=$(numactl --hardware | grep "available:" | awk '{print $2}')
        log "NUMA nodes detected: ${NUMA_NODES}"
        
        if [ "${NUMA_NODES}" -gt 1 ]; then
            warn "Multi-NUMA system detected (${NUMA_NODES} nodes)"
            warn "For benchmarking: consider 'numactl --interleave=all postgres'"
            warn "Add to postgresql.service: ExecStart=numactl --interleave=all ..."
        fi
    fi
}

# =============================================================================
# Transparent Huge Pages
# =============================================================================

configure_thp() {
    log "Disabling Transparent Huge Pages (THP) for PostgreSQL..."
    
    if [ -f /sys/kernel/mm/transparent_hugepage/enabled ]; then
        echo "never" > /sys/kernel/mm/transparent_hugepage/enabled
        echo "never" > /sys/kernel/mm/transparent_hugepage/defrag 2>/dev/null || true
        success "THP disabled"
    else
        warn "THP control file not found"
    fi
    
    # Make persistent via rc.local or systemd
    cat > /etc/systemd/system/disable-thp.service << 'EOF'
[Unit]
Description=Disable Transparent Huge Pages (THP) for PostgreSQL
DefaultDependencies=no
After=sysinit.target local-fs.target
Before=basic.target

[Service]
Type=oneshot
ExecStart=/bin/sh -c "echo never > /sys/kernel/mm/transparent_hugepage/enabled"
ExecStart=/bin/sh -c "echo never > /sys/kernel/mm/transparent_hugepage/defrag"
RemainAfterExit=yes

[Install]
WantedBy=basic.target
EOF
    systemctl daemon-reload
    systemctl enable disable-thp.service 2>/dev/null || true
    success "THP disabled permanently via systemd"
}

# =============================================================================
# File Descriptor Limits
# =============================================================================

configure_ulimits() {
    log "Setting file descriptor limits..."
    
    cat >> /etc/security/limits.d/99-postgres-benchmark.conf << 'EOF'
# MinervaDB PostgreSQL Benchmarking - ulimits
postgres soft nofile 65536
postgres hard nofile 65536
postgres soft nproc 65536
postgres hard nproc 65536
root soft nofile 65536
root hard nofile 65536
* soft nofile 65536
* hard nofile 65536
EOF
    
    success "File descriptor limits configured"
}

# =============================================================================
# Main
# =============================================================================

print_summary() {
    echo ""
    echo -e "${BOLD}System Tuning Summary:${NC}"
    echo "=============================================="
    echo "  Kernel parameters:     /etc/sysctl.d/99-minervadb-postgres-benchmark.conf"
    echo "  I/O scheduler:         Optimized per disk type"
    echo "  CPU governor:          performance"
    echo "  THP:                   disabled"
    echo "  ulimits:               /etc/security/limits.d/99-postgres-benchmark.conf"
    echo "=============================================="
    echo ""
    warn "REBOOT RECOMMENDED to ensure all settings take effect"
    echo ""
}

main() {
    echo -e "${BOLD}MinervaDB PostgreSQL Benchmarking - System Tuning${NC}"
    echo ""
    
    [ "$(id -u)" -ne 0 ] && { echo "Error: Run as root (sudo)"; exit 1; }
    
    apply_kernel_params
    configure_io_scheduler
    configure_cpu_performance
    configure_numa
    configure_thp
    configure_ulimits
    print_summary
    
    success "System tuning complete!"
}

main "$@"
